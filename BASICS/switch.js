// Switch statement
let fruit = "apple"
switch (fruit) {
  case "orange":
    console.log("Oranges are 0.59$ a pound.")
    break
  case "apple":
    console.log("apple are 0.54$ a pound.")
    break
  case "grape":
    console.log("grapes are 0.55$ a pound.")
    break
  default:
    console.log(`Sorry we do not have ${fruit}`)
}
